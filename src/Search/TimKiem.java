package Search;

public class TimKiem {
	private String magv;
	private String makhoa;
	
	public TimKiem() {
		
	}
	
	public TimKiem(String magv, String makhoa) {
		this.magv = magv;
		this.makhoa = makhoa;
	}
	public String getMagv() {
		return magv;
	}
	public void setMagv(String magv) {
		this.magv = magv;
	}
	public String getMakhoa() {
		return makhoa;
	}
	public void setMakhoa(String makhoa) {
		this.makhoa = makhoa;
	}

}
